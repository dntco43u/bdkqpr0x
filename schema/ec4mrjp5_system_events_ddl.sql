  `ID` int UNSIGNED NOT NULL AUTO_INCREMENT COMMENT 'ID'
, `CUSTOMER_ID` bigint DEFAULT NULL COMMENT 'CUSTOMER ID'
, `RECEIVED_AT` datetime DEFAULT NULL COMMENT '로그 수신 시간'
